def text_has_punish_hashtag(text: str | None) -> bool:
    """True если в сообщении есть #punish или #наказать."""
    if not text:
        return False
    return _PUNISH_HASHTAG_RE.search(text) is not None


def extract_punish_target(text: str | None) -> str | None:
    """Достать «цель» наказания из текста команды/хештега (текстовый фолбэк).

    Сначала ищем явный @упоминание (любой токен, начинающийся с @). Если нет —
    берём первый «значимый» токен ПОСЛЕ команды/хештега. Возвращаем как есть
    (с @, если было). None если цель не найдена.

    Реальные @mention-сущности (entities) разбираются в хендлере поверх этого —
    тут чистый разбор текста для тестов и фолбэка.
    """
    if not text:
        return None
    tokens = text.split()
    # 1) первый @-токен
    for tok in tokens:
        if tok.startswith("@") and len(tok) > 1:
            return tok
    # 2) первый токен после команды/хештега
    skip_first = False
    if tokens:
        head = tokens[0].lower()
        if head.startswith("/punish") or head.startswith("#punish") or head == "#наказать":
            skip_first = True
    rest = tokens[1:] if skip_first else tokens
    for tok in rest:
        # пропускаем сами хештеги-триггеры
        low = tok.lower()
        if low in ("#punish", "#наказать") or low.startswith("/punish"):
            continue
        if tok.strip():
            return tok
    return None


def _clean_pool(phrases: list[str] | None) -> list[str]:
    return [p for p in (phrases or []) if p and p.strip()]


def render(
    phrase: str,
    *,
    username: str | None = None,
    target: str | None = None,
    chance_pct: str | None = None,
) -> str:
    """Безопасная подстановка плейсхолдеров (через .replace, не .format).

    Неизвестные «{...}» в кастомной фразе остаются как есть и не роняют
    генерацию. Если значение не передано — соответствующий плейсхолдер не
    трогаем (вызывающий сам решает, когда фраза с плейсхолдером допустима).
    """
    out = phrase
    if username is not None:
        out = out.replace("{username}", username)
    if target is not None:
        out = out.replace("{target}", target)
    if chance_pct is not None:
        out = out.replace("{chance}", chance_pct)
    return out


def choose(
    phrases: list[str] | None,
    use_counts: dict[str, int] | None = None,
) -> str | None:
    """Выбрать ОДНУ СЫРУЮ фразу из пула (без подстановки плейсхолдеров).

    Возвращаем сырую фразу намеренно: её хэш и есть ключ в `use_counts`, так
    что вызывающий должен инкрементить счётчик именно по ней (а отображать —
    через `render`). None если пул пуст.

    Если передан `use_counts` — взвешенный выбор (чередование без частых
    повторов, как у лоха/чухана), иначе честный random.
    """
    pool = _clean_pool(phrases)
    if not pool:
        return None
    if use_counts is not None:
        # Локальный импорт — избегаем цикла worm_master ← phrase_weights.
        from app.services.phrase_weights import weighted_choice

        return weighted_choice(pool, use_counts) or random.choice(pool)
    return random.choice(pool)


def build_announce_extra(
    lines: list[str] | None,
    *,
    username: str,
    chance_pct: str,
) -> str | None:
    """Собрать расширение анонса червя (г). Возвращает многострочный блок или
    None, если пул пуст. Каждая строка — с подстановкой {username}/{chance}.

    Анонс одноразовый → без счётчиков, берём весь пул целиком по порядку.
    """
    pool = _clean_pool(lines)
    if not pool:
        return None
    out = [render(line, username=username, chance_pct=chance_pct) for line in pool]
    return "\n".join(out)


def pick_nag(phrases: list[str] | None, *, username: str) -> str | None:
    """Напоминание про /отвали. Честный random (счётчиков не держим).

    В отличие от `choose`+`render`, сразу подставляет имя — у nag нет
    use_counts, так что сырая фраза вызывающему не нужна.
    """
    pool = _clean_pool(phrases)
    if not pool:
        return None
    return render(random.choice(pool), username=username)
